text = "PythOn"

# to lowercase
lowertxt =text.lower()
print(lowertxt)

# uppercase
uppertxt = text.upper()
print(uppertxt)

# capitalize
capitaltxt = text.capitalize()
print(capitaltxt)

# swapcase(converts uppercase to lower , and lowercase to upper)
swaptxt = text.swapcase()
print(swaptxt)

#removing spaces
#  strip
msg = "   Hello World   "
stripmsg = msg.strip()
print(stripmsg)

# lstrip
lstripmsg = msg.lstrip()
print(lstripmsg)

# rstrip
rstripmsg = msg.rstrip()
print(rstripmsg)